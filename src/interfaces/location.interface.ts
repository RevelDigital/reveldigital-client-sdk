export interface ILocation {

    /**
     * Latitude
     * 
     * @type {number}
     * @memberof ILocation
     */
    latitude: number;

    /**
     * Longitude
     * 
     * @type {number}
     * @memberof ILocation
     */
    longitude: number;

    /**
     * City
     * 
     * @type {string}
     * @memberof ILocation
     */
    city: string;

    /**
     * State
     * 
     * @type {string}
     * @memberof ILocation
     */
    state: string;

    /**
     * Address
     * 
     * @type {string}
     * @memberof ILocation
     */
    address: string;

    /**
     * Country
     * 
     * @type {string}
     * @memberof ILocation
     */
    country: string;

    /**
     * Postal code
     * 
     * @type {string}
     * @memberof ILocation
     */
    postalCode: string;
}
